public class Computation1 { 

    public static void main(String[] args) {

       System.out.print("System.out.println(1+2*3);         ==>       "); System.out.println(1+2*3);
       System.out.print("System.out.println(1);             ==>       "); System.out.println(1);
       System.out.print("System.out.println(1+3);           ==>       "); System.out.println(1+3);
       System.out.print("System.out.println(10-20);         ==>       "); System.out.println(10-20);
       System.out.print("System.out.println(-10-20);        ==>       "); System.out.println(-10-20);
       System.out.print("System.out.println(1+3*2);         ==>       "); System.out.println(1+3*2);
       System.out.print("System.out.println(1+(3*2));       ==>       "); System.out.println(1+(3*2));
       System.out.print("System.out.println((1+3)*2);       ==>       "); System.out.println((1+3)*2);
       System.out.print("System.out.println(1+3*2/5.0);     ==>       "); System.out.println(1+3*2/5.0);
       System.out.print("System.out.println((1+3)*2/5.0);   ==>       "); System.out.println((1+3)*2/5.0);
       System.out.print("System.out.println((1+3)*(2/5.0)); ==>       "); System.out.println((1+3)*(2/5.0));

       // % computes the remainder in integer division
       System.out.print("System.out.println(5%3);           ==>       "); System.out.println(5%3);
       System.out.print("System.out.println(11%4);          ==>       "); System.out.println(11%4);
       System.out.print("System.out.println(2/5);          ==>       "); System.out.println(2/5);
       System.out.print("System.out.println(8/5);          ==>       "); System.out.println(8/5);
   }
}
