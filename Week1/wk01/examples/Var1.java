/** Variables
 *

 *  Programming in Java makes strong use of variables. Variables have
 *  a name, a type, and a value.

 *  Type and name are fixed, but the value can be changed. Variables
 *  are declared by a type declaration and the name, e.g., int x;
 *  The value is set (or changed) by an assignment, e.g., x = 3;
 *  Read: `x becomes 3' or `x is set to 3'.
 *  Note: Although the assignment uses the equality symbol `=', it must be
 *        distinguished from equal. That is, x = 3; DOES NOT MEAN 
 *        `x is equal to 3'.
 *  Declaration and assignment can be combined as in the example below:
 *  int x = 3;
 *
 *  @author  Manfred Kerber
 *  @version 2014-09-27
 */


public class Var1 { 
    public static void main(String[] args) {
       int x = 3;
       System.out.println(x);
   }
}
