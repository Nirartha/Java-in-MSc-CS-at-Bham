public class Computation3 { 

    public static void main(String[] args) {
       System.out.println();
       System.out.print("System.out.println(Math.PI);                ==>  "); System.out.println(Math.PI);
       System.out.print("System.out.println(Math.E);                 ==>  "); System.out.println(Math.E);
       System.out.println();
       System.out.print("System.out.println(Math.sin(1));            ==>  "); System.out.println(Math.sin(1));
       System.out.print("System.out.println(Math.sin(Math.PI/2.0));  ==>  "); System.out.println(Math.sin(Math.PI/2.0));
       System.out.print("System.out.println(Math.sin(Math.PI));      ==>  "); System.out.println(Math.sin(Math.PI));

       System.out.print("System.out.println(Math.cos(1));            ==>  "); System.out.println(Math.cos(1));
       System.out.print("System.out.println(Math.cos(Math.PI/2.0));  ==>  "); System.out.println(Math.cos(Math.PI/2.0));
       System.out.print("System.out.println(Math.cos(Math.PI));      ==>  "); System.out.println(Math.cos(Math.PI));

       System.out.print("System.out.println(Math.tan(1));            ==>  "); System.out.println(Math.tan(1));
       System.out.print("System.out.println(Math.tan(Math.PI/2.0));  ==>  "); System.out.println(Math.tan(Math.PI/2.0));
       System.out.print("System.out.println(Math.tan(Math.PI));      ==>  "); System.out.println(Math.tan(Math.PI));

       System.out.print("System.out.println(Math.atan(1));           ==>  "); System.out.println(Math.atan(1));
       System.out.print("System.out.println(Math.asin(1));           ==>  "); System.out.println(Math.asin(1));
       System.out.print("System.out.println(Math.acos(1));           ==>  "); System.out.println(Math.acos(1));

       System.out.print("System.out.println(Math.atan(2));           ==>  "); System.out.println(Math.atan(2));

   }
}
